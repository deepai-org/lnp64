pub fn find_token_constant(name: &str) -> Option<i64> {
    match name {
        "JSMN_UNDEFINED" => Some(0),
        "JSMN_OBJECT" => Some(1),
        "JSMN_ARRAY" => Some(2),
        "JSMN_STRING" => Some(4),
        "JSMN_PRIMITIVE" => Some(8),
        "JSMN_ERROR_NOMEM" => Some(-1),
        "JSMN_ERROR_INVAL" => Some(-2),
        "JSMN_ERROR_PART" => Some(-3),
        "PRIM" => Some(0),
        "LPAR" => Some(1),
        "RPAR" => Some(2),
        "NOT" => Some(3),
        "AND" => Some(4),
        "OR" => Some(5),
        "END" => Some(6),
        "IGNORE" => Some(0),
        "EVERY" => Some(1),
        "LINE" => Some(2),
        "LAST" => Some(3),
        "REGEX" => Some(4),
        "LASTRE" => Some(5),
        "PATH_MAX" => Some(4096),
        "_SC_LOGIN_NAME_MAX" => Some(256),
        "LOGIN_NAME_MAX" => Some(256),
        "F_OK" => Some(0),
        "EEXIST" => Some(17),
        "EXDEV" => Some(18),
        "ENOSYS" => Some(38),
        "SEEK_SET" => Some(0),
        "SEEK_CUR" => Some(1),
        "SEEK_END" => Some(2),
        "S_ISUID" => Some(0o4000),
        "S_ISGID" => Some(0o2000),
        "S_ISVTX" => Some(0o1000),
        "S_IRUSR" => Some(0o0400),
        "S_IWUSR" => Some(0o0200),
        "S_IXUSR" => Some(0o0100),
        "S_IRGRP" => Some(0o0040),
        "S_IWGRP" => Some(0o0020),
        "S_IXGRP" => Some(0o0010),
        "S_IROTH" => Some(0o0004),
        "S_IWOTH" => Some(0o0002),
        "S_IXOTH" => Some(0o0001),
        "REG_EXTENDED" => Some(1),
        "REG_ICASE" => Some(2),
        "REG_NOSUB" => Some(4),
        "REG_NEWLINE" => Some(8),
        _ => None,
    }
}
