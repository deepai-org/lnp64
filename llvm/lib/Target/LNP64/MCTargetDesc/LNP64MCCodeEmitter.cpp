//===-- LNP64MCCodeEmitter.cpp - v2 64-bit encoder -----------------------===//
//
// TableGen-driven v2 encoder. Every instruction is one 8-byte little-endian
// word whose bit layout is declared by the `field bits<64> Inst` in
// LNP64InstrInfo.td. getBinaryCodeForInstr (generated into
// LNP64GenMCCodeEmitter.inc) fills the register/immediate fields; the custom
// operand encoders below handle the branch/jump targets that need a >>3 shift
// plus a relocation, and the AUIPC U-type target.
//
//   opcode[63:56] rd[55:51] rs1[50:46] rs2[45:41] rs3[40:36] rs4[35:31]
//   rs5[30:26]; I-type imm32 [45:14]; S/B-type imm32 [40:9]; U/J-type [50:19].
//
//===----------------------------------------------------------------------===//

#include "LNP64MCTargetDesc.h"
#include "llvm/MC/MCCodeEmitter.h"
#include "llvm/MC/MCFixup.h"
#include "llvm/MC/MCInst.h"
#include "llvm/MC/MCInstrInfo.h"
#include "llvm/MC/MCRegisterInfo.h"
#include "llvm/MC/MCSubtargetInfo.h"
#include "llvm/Support/ErrorHandling.h"
#include "llvm/Support/raw_ostream.h"

using namespace llvm;

namespace {

class LNP64MCCodeEmitter final : public MCCodeEmitter {
  const MCInstrInfo &MCII;

public:
  LNP64MCCodeEmitter(const MCInstrInfo &MCII) : MCII(MCII) {}

  void encodeInstruction(const MCInst &MI, raw_ostream &OS,
                         SmallVectorImpl<MCFixup> &Fixups,
                         const MCSubtargetInfo &STI) const override {
    uint64_t Word = getBinaryCodeForInstr(MI, Fixups, STI);
    char Bytes[8];
    for (unsigned I = 0; I < 8; ++I)
      Bytes[I] = static_cast<char>(Word >> (8 * I));
    OS.write(Bytes, sizeof(Bytes));
  }

  // Generated by TableGen (-gen-emitter): the full 64-bit instruction word.
  uint64_t getBinaryCodeForInstr(const MCInst &MI,
                                 SmallVectorImpl<MCFixup> &Fixups,
                                 const MCSubtargetInfo &STI) const;

  // Default operand encoder: registers return their HWEncoding (GPR r0..r31 ->
  // 0..31, PCR -> 0..11), immediates return their value (masked to field width
  // by the generated code).
  uint64_t getMachineOpValue(const MCInst &MI, const MCOperand &MO,
                             SmallVectorImpl<MCFixup> &Fixups,
                             const MCSubtargetInfo &STI) const {
    if (MO.isReg())
      return Ctx_getRegEncoding(MO.getReg());
    if (MO.isImm())
      return static_cast<uint64_t>(MO.getImm());
    llvm_unreachable("unexpected operand kind in LNP64 encoder");
  }

  // B-type compare-and-branch target: pc-relative, encoded as (off >> 3); an
  // expr emits a branch relocation (the imm field stays zero).
  uint64_t getBranchTargetOpValue(const MCInst &MI, unsigned OpNo,
                                  SmallVectorImpl<MCFixup> &Fixups,
                                  const MCSubtargetInfo &STI) const {
    const MCOperand &MO = MI.getOperand(OpNo);
    if (MO.isImm())
      return static_cast<uint64_t>(MO.getImm() >> 3);
    Fixups.push_back(MCFixup::create(
        0, MO.getExpr(), MCFixupKind(LNP64::fixup_lnp64_branch)));
    return 0;
  }

  // J-type jump/jal target: pc-relative, encoded as (off >> 3); an expr emits a
  // jump relocation.
  uint64_t getJumpTargetOpValue(const MCInst &MI, unsigned OpNo,
                                SmallVectorImpl<MCFixup> &Fixups,
                                const MCSubtargetInfo &STI) const {
    const MCOperand &MO = MI.getOperand(OpNo);
    if (MO.isImm())
      return static_cast<uint64_t>(MO.getImm() >> 3);
    Fixups.push_back(MCFixup::create(
        0, MO.getExpr(), MCFixupKind(LNP64::fixup_lnp64_jump)));
    return 0;
  }

  // U-type AUIPC target: an imm goes straight into the field; an expr emits an
  // auipc relocation.
  uint64_t getAUIPCTargetOpValue(const MCInst &MI, unsigned OpNo,
                                 SmallVectorImpl<MCFixup> &Fixups,
                                 const MCSubtargetInfo &STI) const {
    const MCOperand &MO = MI.getOperand(OpNo);
    if (MO.isImm())
      return static_cast<uint64_t>(MO.getImm());
    Fixups.push_back(MCFixup::create(
        0, MO.getExpr(), MCFixupKind(LNP64::fixup_lnp64_auipc)));
    return 0;
  }

private:
  static unsigned Ctx_getRegEncoding(unsigned Reg);
};

unsigned LNP64MCCodeEmitter::Ctx_getRegEncoding(unsigned Reg) {
  // GPRs are contiguous; their HWEncoding equals (Reg - R0). PCRs encode 0..11.
  if (Reg >= LNP64::R0 && Reg <= LNP64::R31)
    return Reg - LNP64::R0;
  switch (Reg) {
  case LNP64::PID: return 0;
  case LNP64::PPID: return 1;
  case LNP64::TID: return 2;
  case LNP64::TP: return 3;
  case LNP64::UID: return 4;
  case LNP64::GID: return 5;
  case LNP64::SIGMASK: return 6;
  case LNP64::SIGPENDING: return 7;
  case LNP64::REALTIME_SEC: return 8;
  case LNP64::REALTIME_NSEC: return 9;
  case LNP64::CRED_PROFILE: return 10;
  case LNP64::CRED_HANDLE: return 11;
  default:
    llvm_unreachable("expected LNP64 GPR or PCR operand");
  }
}

#include "LNP64GenMCCodeEmitter.inc"

} // end anonymous namespace

MCCodeEmitter *llvm::createLNP64MCCodeEmitter(const MCInstrInfo &MCII,
                                             const MCRegisterInfo &,
                                             MCContext &) {
  return new LNP64MCCodeEmitter(MCII);
}
